int	ft_str_is_numeric(char *str)
{
	if (*str == NULL)
	{
		return (1);
	}
	while (*str != '\0')
	{
		if (*str <= '0' || *str >= '9')
		{
			return (0);
		}
		str++;
	}
	return (1);
}
