int	ft_strcmp(char *s1, char *s2)
{
	if (*s1 == NULL || *s2 == NULL)
		return (0);
	if (*s1 < *s2)
		return (1);
	if (*s1 > *s2)
		return (-1);
	else
		ft_strcmp(s1 + 1, s2 + 1);
}
