int	ft_str_is_alpha(char *str)
{
	if (*str == NULL)
	{
		return (1);
	}
	while (*str != '\0')
	{
		if (('z' <= *str || *str <= 'a') || ('Z' <= *str || *str <= 'A'))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
